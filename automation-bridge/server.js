function nowIso() {
  return new Date().toISOString();
}

function makeId() {
  const id = `task_${Date.now()}_${seq}`;
  seq += 1;
  return id;
}

function pushLog(task, message) {
  task.logs.push({
    at: nowIso(),
    message: String(message || ""),
  });
}

function addHistoryEvent(task, type, message, meta = {}) {
  if (!Array.isArray(task.history)) {
    task.history = [];
  }
  task.history.push({
    timestamp: nowIso(),
    type,
    message: String(message || ""),
    meta: isPlainObject(meta) ? meta : {},
  });
}


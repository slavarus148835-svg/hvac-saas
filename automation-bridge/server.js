// Return JSON for malformed JSON bodies instead of HTML.
app.use((err, _req, res, next) => {
  if (err && err.type === "entity.parse.failed") {
    return res.status(400).json({
      ok: false,
      error: "Invalid JSON body",
    });
  }
  return next(err);
});


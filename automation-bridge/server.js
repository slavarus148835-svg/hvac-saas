async function requestRevisionForTask(task, reason, instructions, requestedBy) {
  task._telegramExecutionOutcomeSent = false;
  if (task.result) {
    task.previousResults = Array.isArray(task.previousResults)
      ? [...task.previousResults, task.result]
      : [task.result];
    task.result = null;
  }

  task.revisionCount = typeof task.revisionCount === "number" ? task.revisionCount + 1 : 1;
  const revision = {
    revisionId: `rev-${task.revisionCount}`,
    createdAt: nowIso(),
    reason: reason.trim(),
    instructions: instructions.trim(),
    requestedBy:
      typeof requestedBy === "string" && requestedBy.trim()
        ? requestedBy.trim()
        : "controller",
    status: "queued",
  };

  task.revisions = Array.isArray(task.revisions)
    ? [...task.revisions, revision]
    : [revision];
  task.latestRevision = revision;
  task.status = "queued";
  task.reviewStatus = "pending";
  task.controllerDecision = "revise";
  task.lifecycleStage = "revision_requested";
  task.lastAction = "revision_requested";
  task.updatedAt = nowIso();
  pushLog(task, "revision requested");
  addHistoryEvent(task, "revision_requested", "Revision requested", {
    revisionId: revision.revisionId,
    reason: revision.reason,
  });
  tasks.set(task.id, task);

  if (task.source === "telegram") {
    void sendTelegramTaskUpdate(task, "revise", { logger: console }).catch(() => {});
  }

  if (BRIDGE_MODE === "real") {
    await runRealTask(task, tasks);
    pushLog(task, "task re-submitted to Cursor adapter");
    addHistoryEvent(task, "execution_started", "Revision re-submitted to Cursor adapter");
    task.updatedAt = nowIso();
    tasks.set(task.id, task);
  } else {
    // Keep immediate response in revision_requested stage.
    setTimeout(async () => {
      const current = tasks.get(task.id);
      if (!current) return;
      try {
        await runTask(current, tasks);
      } catch (error) {
        current.status = "failed";
        current.agentStatus = "failed";
        current.lifecycleStage = "failed";
        current.lastAction = "task_failed";
        if (current.latestRevision) {
          current.latestRevision = { ...current.latestRevision, status: "failed" };
        }
        current.updatedAt = nowIso();
        pushLog(current, `Revision run error: ${error.message || "unknown error"}`);
        addHistoryEvent(current, "task_failed", "Revision run failed", {
          error: error.message || "unknown error",
        });
        tasks.set(current.id, current);
      }
    }, 0);
  }
}

async function runAutoControllerReview(task) {
  if (!task || task.status !== "done") return;
  if (task._autoReviewInProgress) return;
  task._autoReviewInProgress = true;

  try {
    const shouldRevise = hasExecutionIssues(task);
    if (!shouldRevise) {
      applyControllerReviewDecision(
        task,
        "approve",
        "Auto review approved successful result",
        "No further action"
      );
      pushLog(task, "auto review: approved");
      task.updatedAt = nowIso();
      tasks.set(task.id, task);
      return;
    }

    pushLog(task, "auto review: revise triggered");
    task._suppressTelegramReviseFromController = true;
    applyControllerReviewDecision(
      task,
      "revise",
      "Auto review detected errors or empty result",
      "Regenerate and fix task output"
    );
    task._suppressTelegramReviseFromController = false;
    task.updatedAt = nowIso();
    tasks.set(task.id, task);
    await requestRevisionForTask(
      task,
      "Auto review detected errors or empty result",
      "Fix errors and provide non-empty result.summary/changedFiles/checks",
      "controller"
    );
  } finally {
    task._autoReviewPending = false;
    task._autoReviewInProgress = false;
    tasks.set(task.id, task);
  }
}


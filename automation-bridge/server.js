async function tryNotifyTelegramExecutionOutcome(task) {
  const current = tasks.get(task.id);
  if (!current || current.source !== "telegram" || !current.telegramChatId) return;
  if (current._telegramExecutionOutcomeSent) return;
  if (current.status === "done") {
    if (!isPlainObject(current.result) || Object.keys(current.result).length === 0) return;
    current._telegramExecutionOutcomeSent = true;
    tasks.set(current.id, current);
    await sendTelegramTaskUpdate(current, "execution_done", { logger: console });
    return;
  }
  if (current.status === "failed") {
    current._telegramExecutionOutcomeSent = true;
    tasks.set(current.id, current);
    const lastLog =
      Array.isArray(current.logs) && current.logs.length
        ? String(current.logs[current.logs.length - 1].message || "")
        : "";
    await sendTelegramTaskUpdate(current, "execution_error", {
      logger: console,
      errorText: lastLog || "Задача завершилась с ошибкой",
    });
  }
}


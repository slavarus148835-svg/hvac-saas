function stopTelegramRuntime() {
  try {
    telegramRuntime.stop();
  } catch (_e) {
    // ignore
  }
  telegramRuntime = { stop() {}, enabled: false };
}

function startTelegramRuntimeFromProcessEnv() {
  const { token, chatId } = getTelegramEnv();
  if (!token) {
    return;
  }
  stopTelegramRuntime();
  telegramRuntime = startTelegramBot({
    token,
    allowedChatId: chatId,
    logger: console,
    onQuickTask: async (text, meta) => {
      const quickRun = await runQuickTaskFromText(text, {
        sourceLabel: "telegram bot",
        source: "telegram",
        telegramChatId: meta && meta.chatId != null ? String(meta.chatId) : null,
        telegramMessageId: meta && meta.messageId != null ? meta.messageId : null,
        triggerLog: "telegram task triggered",
        planEventMessage: "Auto-plan generated by telegram bot",
        executionEventMessage: "Execution started from telegram bot",
      });
      if (!quickRun.ok || !quickRun.task) {
        return `Ошибка запуска задачи: ${quickRun.error || "unknown error"}`;
      }
      const t = quickRun.task;
      recordTelegramEvent(
        `task created from telegram id=${t.id} source=${t.source} telegramChatId=${t.telegramChatId} telegramMessageId=${t.telegramMessageId ?? "null"}`
      );
      return formatTelegramTaskLaunch(quickRun.task);
    },
    onStatus: async (taskIdRaw) => {
      const taskId = String(taskIdRaw || "").trim();
      if (!taskId) {
        return "Укажите taskId: /status <taskId>";
      }
      const task = tasks.get(taskId);
      if (!task) {
        return `Task not found: ${taskId}`;
      }
      return formatTelegramTaskStatus(task);
    },
    onLast: async () => {
      const task = getLatestTask();
      if (!task) {
        return "Задач пока нет";
      }
      return formatTelegramLastTask(task);
    },
    onHelp: async () => telegramHelpText(),
  });
}

async function tryAutoFillTelegramChatIdFromUpdates() {
  reloadProcessEnvFromFile();
  const { token, chatId } = getTelegramEnv();
  if (!token || chatId) {
    return { updated: false, reason: chatId ? "chat_already_set" : "no_token" };
  }
  const result = await getLastChatIdFromUpdates(token);
  if (!result.ok || !result.chatId) {
    return { updated: false, probe: result };
  }
  ensureEnvFileExists(ENV_FILE_PATH);
  upsertEnvKey(ENV_FILE_PATH, "TELEGRAM_ALLOWED_CHAT_ID", result.chatId);
  reloadProcessEnvFromFile();
  console.log(`telegram: auto-filled TELEGRAM_ALLOWED_CHAT_ID from getUpdates`);
  return { updated: true, chatId: result.chatId };
}


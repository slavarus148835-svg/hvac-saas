    status: task.status,
    agentStatus: task.agentStatus,
    reviewStatus: task.reviewStatus,
    controllerDecision: task.controllerDecision,
    changedFiles: Array.isArray(safeResult.changedFiles) ? safeResult.changedFiles : [],

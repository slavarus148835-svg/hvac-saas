// POST /task-result
// Simplified callback for local watcher / external agent (taskId + status + payload).
app.post("/task-result", (req, res) => {
  const body = req.body || {};
  const taskId = typeof body.taskId === "string" ? body.taskId.trim() : "";
  if (!taskId) {
    return res.status(400).json({ ok: false, error: "taskId is required" });
  }
  const task = tasks.get(taskId);
  if (!task) {
    return res.status(404).json({ ok: false, error: "Task not found" });
  }

  const statusRaw = String(body.status || "").toLowerCase();
  const isError = statusRaw === "error" || statusRaw === "failed";
  const isDone = statusRaw === "done" || statusRaw === "success";
  if (!isError && !isDone) {
    return res.status(400).json({ ok: false, error: 'status must be "done" or "error"' });
  }

  const summary = typeof body.summary === "string" ? body.summary : "";
  const changedFiles = Array.isArray(body.changedFiles)
    ? body.changedFiles.filter((f) => typeof f === "string")
    : [];
  const checks = Array.isArray(body.checks)
    ? body.checks.filter((f) => typeof f === "string")
    : [];
  const notes = Array.isArray(body.notes) ? body.notes.filter((f) => typeof f === "string") : [];

  const logs = body.logs;
  if (Array.isArray(logs)) {
    for (const item of logs) {
      if (typeof item === "string" && item.trim()) {
        pushLog(task, item);
      } else if (item && typeof item.message === "string" && item.message.trim()) {
        pushLog(task, item.message);
      }
    }
  }

  if (isError) {
    const errText =
      typeof body.error === "string" && body.error.trim()
        ? body.error.trim()
        : "Task error";
    task.status = "failed";
    task.agentStatus = "failed";
    task.lifecycleStage = "failed";
    task.lastAction = "task_failed";
    if (task.latestRevision) {
      task.latestRevision = { ...task.latestRevision, status: "failed" };
    }
    pushLog(task, errText);
    addHistoryEvent(task, "task_failed", "task-result: failed", { error: errText });
    task._autoReviewPending = false;
  } else {
    task.status = "done";
    task.agentStatus = "finished";
    task.lifecycleStage = "reported";
    task.lastAction = "task_completed";
    if (task.latestRevision) {
      task.latestRevision = { ...task.latestRevision, status: "done" };
    }
    task.result = {
      summary:
        summary ||
        `Completed for goal: ${String(task.goal || task.title || taskId).slice(0, 200)}`,
      changedFiles,
      checks,
      notes,
    };
    addHistoryEvent(task, "task_completed", "task-result: done");
    task._autoReviewPending = true;
  }

  task.updatedAt = nowIso();
  tasks.set(task.id, task);
  void tryNotifyTelegramExecutionOutcome(tasks.get(task.id)).catch(() => {});
  return res.json({ ok: true, task: sanitizeTask(task) });
});


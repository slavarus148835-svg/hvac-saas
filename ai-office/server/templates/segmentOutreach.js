function pick(seed, arr) {
  const s = String(seed || "x");
  let h = 0;
  for (let i = 0; i < s.length; i += 1) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  return arr[h % arr.length];
}

function base(lead) {
  const org = (lead.name || "объект").replace(/\s+/g, " ").trim();
  const city = lead.city || "";
  const sum = (txt) => txt.replace(/\{org\}/g, org).replace(/\{city\}/g, city);
  return { org, city, sum };
}


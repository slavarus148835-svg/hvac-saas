function segmentFallback(lead, offer) {
  const seg = String(lead.segment || "");
  if (seg === "business_center") return bc(lead, offer);
  if (seg === "uk") return ukTpl(lead, offer);
  if (seg === "hotel") return hotel(lead, offer);
  if (seg === "clinic") return clinic(lead, offer);
  if (seg === "restaurant") return restaurant(lead, offer);
  if (seg === "office") return office(lead, offer);
  if (seg === "store") return store(lead, offer);
  if (seg === "warehouse") return warehouse(lead, offer);
  if (seg === "fitness") return fitness(lead, offer);
  return generic(lead, offer);
}

